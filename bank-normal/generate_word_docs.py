    doc.add_paragraph('店舗のデジタル完結化が進むとともに、銀行の機能がAPIを通じて他社のサービスに組み込まれる（BaaS）時代が到来している。これにより、生活のあらゆる場面に金融機能が溶け込む「インビジブル・バンキング（見えない銀行）」へと進化していく。')

    doc.save('bank-nomal-document.docx')

